    external = status.get("external_witness_records") or {}
    external_count = external.get("external_witness_index_record_count", 0)
    if not isinstance(external_count, int):
        external_count = 0
    external_counts = external.get("external_witness_index_counts") or {}
    notarial_count = external_counts.get("notarial_record", 0)
