def sha256_text(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def fail(message: str, code: int = 2) -> None:
    print(f"ERROR: {message}", file=sys.stderr)
    sys.exit(code)


def validate_guardian_id(value: str) -> None:
    if not re.fullmatch(r"guardian_ed25519_[a-f0-9]{16}", value or ""):
        fail(f"Invalid guardian_id: {value}")


def validate_sha256(value: str, field: str) -> None:
    if not re.fullmatch(r"[a-f0-9]{64}", value or ""):
        fail(f"Invalid {field}: {value}")


def forbid_registry_number_text(value: str) -> None:
    if not value:
        return
    lowered = value.lower()
    if "guardian_registry_number" in lowered:
        fail("Do not include guardian_registry_number in listing request input. Number is system-generated.")


