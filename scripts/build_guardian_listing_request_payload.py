def write_submit_lock(out_path: Path) -> None:
    """Write a .submit-lock.json file alongside the signed payload."""
    payload = json.loads(out_path.read_text(encoding="utf-8"))
    proof = payload.get("authorship_proof") or {}
    lock = {
        "schema": "trinityaccord.guardian-listing-submit-lock.v1",
        "payload_file": str(out_path),
        "payload_profile": GUARDIAN_LISTING_PAYLOAD_PROFILE,
        "expected_builder": GUARDIAN_STAGE_2_EXPECTED_BUILDER,
        "wrong_builders": GUARDIAN_STAGE_2_WRONG_BUILDERS,
        "signed_payload_sha256": proof.get("signed_payload_sha256"),
        "do_not_edit_after_signing": True,
        "submit_exact_generated_file": True,
        "if_modified": "Discard edited JSON and rerun scripts/build_guardian_listing_request_payload.py",
        "if_gateway_rejects_counts_toward_home_after_local_pass": "Gateway deployment/schema is stale; update Gateway rather than changing this payload.",
        "gateway_contract_version": GUARDIAN_STAGE_2_GATEWAY_CONTRACT_VERSION,
        "authorship_canonical_version": AUTHORSHIP_CANONICAL_VERSION,
        "required_gateway_capabilities": GUARDIAN_STAGE_2_REQUIRED_GATEWAY_CAPABILITIES,
        "gateway_capability_check": "Run: python3 scripts/diagnose_guardian_listing_payload.py --gateway-base-url <url> <payload>",
    }
    lock_path = out_path.with_suffix(out_path.suffix + ".submit-lock.json")
    lock_path.write_text(json.dumps(lock, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"Submit lock written to {lock_path}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Build active Guardian registry listing request payload")
    parser.add_argument("--agent-name", required=True)
    parser.add_argument("--provider", required=True)
    parser.add_argument("--source-issue", type=int, required=True)
    parser.add_argument("--guardian-id", required=True)
    parser.add_argument("--public-key-sha256", required=True)
    parser.add_argument("--label", required=True)
    parser.add_argument(
        "--guardian-type",
        default="human_with_ai_agent",
        choices=["ai_agent", "human", "human_with_ai_agent", "automated_script"],
    )
    parser.add_argument("--application-mode", default="joint_human_ai")
    parser.add_argument("--title", default=None)
    parser.add_argument("--idempotency-key", default=None)
    parser.add_argument("--out", required=True)
    parser.add_argument(
        "--reception-initiation-class",
        default="externally_requested",
        choices=sorted(ALLOWED_RECEPTION_INITIATION_CLASSES),
    )
    parser.add_argument(
        "--reception-initiation-basis",
        default="explicit_verification_request",
        choices=sorted(ALLOWED_RECEPTION_INITIATION_BASIS),
    )
    parser.add_argument("--human-claimed-name", default=None)
    parser.add_argument("--agent-claimed-id", default=None)
    parser.add_argument("--agent-instance-id", default=None)
    parser.add_argument("--agent-public-profile", default=None)
    add_authorship_arguments(parser)
    args = parser.parse_args()

    payload = build_payload(args)

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(payload, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"Payload written to {out_path}")

    attach_authorship_default_or_requested(args, out_path, payload=payload)
    write_submit_lock(out_path)

    print("\n--- Payload Validator ---")
    result = subprocess.run(
        ["python3", "scripts/validate_gateway_payload.py", str(out_path)],
        cwd=str(ROOT),
    )
    if result.returncode != 0:
        fail("Payload validation FAILED", code=1)
    print("Payload validation PASS")

    print("\n--- Archive Readiness Gate ---")
    result = subprocess.run(
        ["python3", "scripts/archive_readiness_gate.py", "--gateway-payload", str(out_path), "--json"],
        cwd=str(ROOT),
    )
    if result.returncode != 0:
        fail("Archive readiness gate FAILED", code=1)
    print("Archive readiness gate PASS")

    print("\nActive Guardian registry listing request payload is valid and archive-ready.")


if __name__ == "__main__":
    main()
